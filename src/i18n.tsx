export type TFunction = (key: TranslationKey) => string;

export function isAppLanguage(value: unknown): value is AppLanguage {
  return typeof value === 'string' && supportedLanguages.includes(value as AppLanguage);
}

export function createTranslator(language: AppLanguage): TFunction {
  return (key) => dictionaries[language][key] ?? en[key] ?? key;
}

export function translateOption(value: string, t: TFunction) {
  return optionKeyByValue[value as keyof typeof optionKeyByValue]
    ? t(optionKeyByValue[value as keyof typeof optionKeyByValue])
    : value;
}

export function translateSettingsCategory(value: string, t: TFunction) {
  return settingsCategoryKeyByValue[value as keyof typeof settingsCategoryKeyByValue]
    ? t(settingsCategoryKeyByValue[value as keyof typeof settingsCategoryKeyByValue])
    : value;
}

const I18nContext = createContext<{ language: AppLanguage; t: TFunction }>({
  language: defaultLanguage,
  t: createTranslator(defaultLanguage),
});

export function I18nProvider({ children, language }: { children: ReactNode; language: AppLanguage }) {
  const value = useMemo(() => ({ language, t: createTranslator(language) }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
